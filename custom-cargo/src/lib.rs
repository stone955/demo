/// Increment one to the number given.
///
/// # Examples
/// ```
/// fn main() {
///     println!("Hello, world!");
/// }
/// ```
/// # Panics
///
/// # Errors
///
/// # Safety
///
pub fn increment(x: i32) -> i32 {
    x + 1
}